<h3 class="tile-title">Features</h3>
            <ul>
              <li>The app can send real-time alerts and notifications about weather-related disasters (e.g., hurricanes, 
                tornadoes, floods), geological events (e.g., earthquakes, tsunamis), 
                or other emergencies (e.g., wildfires, pandemics) in the user's geographical area.</li>
              <li>Access to emergency contact numbers, including local authorities, hospitals, fire departments, and law enforcement agencies.</li>
              <li>The app offers safety guidelines, tips, and recommendations for different types of disasters, such as what to do during earthquakes, hurricanes, or wildfires.</li>
              <li>Integration with Geographic Information Systems (GIS) to provide mapping, geolocation, and tracking features.</li>
              <li>Tools for reporting incidents or damage during and after disasters.</li>
              <li>Widgets to effectively display statistics</li>
              <li>Data tables with sort, search and paginate functionality</li>
              <li>Custom form elements like toggle buttons, auto-complete, tags and date-picker</li>
              <li>A inbuilt toast library for providing meaningful response messages to user's actions</li>
            </ul>
